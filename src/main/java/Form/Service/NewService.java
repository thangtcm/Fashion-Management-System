    private void init(TypeInterface type)
    {
        if(type == TypeInterface.Create)
        {
            TitleForm.setText("Create Service");
            txtServiceName.setText("");
            txtServicePrice.setText("");
            txtServiceDescription.setText("");
            btnSave.setText("Create");
        }
        if(type == TypeInterface.Edit)
        {
            TitleForm.setText("Edit Service");
            txtServiceName.setText(this.services.getServiceName());
            txtServicePrice.setText(String.valueOf(this.services.getServicePrice()));
            txtServiceDescription.setText(this.services.getServiceDescription());
            btnSave.setText("Save");
        }
    }
    
    private boolean initData()
    {
        Service service = new Service();
        service.setServiceName(txtServiceName.getText());
        service.setServicePrice(Double.parseDouble(txtServicePrice.getText()));
        service.setServiceDescription(txtServiceDescription.getText());
        Service_Dao service_Dao = new Service_DaoImpl();
        
        if(this.type == TypeInterface.Create)
        {
            if(service_Dao.AddService(service))
            {
                showMessage("Bạn vừa tạo mới dịch vụ thành công", TypeNotification.Success);
                return true;
            }  
            else
            {
                showMessage("Đã xảy ra lỗi", TypeNotification.Error);
            }
            
            return true;
        }
        else if(this.type == TypeInterface.Edit)
        {
            service.setID(this.services.getID());
            if(service_Dao.Update_Service(service))
            {
                showMessage("Bạn vừa cập nhật dịch vụ thành công", TypeNotification.Success);
                return true;
            }  
            else
            {
                showMessage("Đã xảy ra lỗi", TypeNotification.Error);
            }  
        }
        return false;
    }
    
